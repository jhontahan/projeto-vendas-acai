    private void preencherCampos(Funcionarios f){
        campoCodigo.setText(""+f.getId());
        campoNome.setText(f.getNome());
        campoRG.setText(f.getRg());
        campoCPF.setText(f.getCpf());
        campoLogin.setText(f.getLogin());
        campoSenha.setText(f.getSenha());
        campoCargo.setText(f.getCargo());
        comboAcesso.setSelectedItem(f.getNivelAcesso());
        campoTelefone.setText(f.getTelefone());
        campoCelular.setText(f.getCelular());
        campoCep.setText(f.getCep());
        campoEndereco.setText(f.getEndereco());
        campoNumero.setText(f.getNumero() != null ? ""+f.getNumero() : "");
        campoComplemento.setText(f.getComplemento());
        campoBairro.setText(f.getBairro());
        campoCidade.setText(f.getCidade());
        comboUf.setSelectedItem(f.getEstado());
        
    }
    
