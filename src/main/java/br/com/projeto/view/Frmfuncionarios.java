       funcionario.setTelefone(campoTelefone.getText());
       funcionario.setCelular(campoCelular.getText());
       funcionario.setCep(campoCep.getText());
       funcionario.setEndereco(campoEndereco.getText());
       
       if (!StringUtils.isBlank(campoNumero.getText())){
           funcionario.setNumero(Integer.parseInt(campoNumero.getText()));
       }
//       funcionario.setNumero(campoNumero.getText() != null ? Integer.parseInt(campoNumero.getText()) : null);;
