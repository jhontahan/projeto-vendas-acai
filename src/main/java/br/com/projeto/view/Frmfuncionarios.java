       funcionario.setComplemento(campoComplemento.getText());
       funcionario.setBairro(campoBairro.getText());
       funcionario.setCidade(campoCidade.getText());
       funcionario.setEstado(comboUf.getSelectedItem().toString());
        
       FuncionarioDAO.getInstance().persist(funcionario);
       
