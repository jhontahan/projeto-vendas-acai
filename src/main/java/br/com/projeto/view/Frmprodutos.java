    public void listar(){
        List<Produto> lista = new ArrayList<>();
        
        lista = ProdutoDAO.getInstance().findBy(null);
        
        DefaultTableModel dados = (DefaultTableModel) tabelaProdutos.getModel();
        dados.setNumRows(0);
        
        for (Produto f : lista){
             String dataFormatada = DateTimeFormatter.ofPattern("dd/MM/yyyy HH:mm:ss").format(f.getDataCadastro());
            dados.addRow(new Object[]{
                f.getId(),
                f.getDescricao(),
                dataFormatada
            });
        }
        
    }

