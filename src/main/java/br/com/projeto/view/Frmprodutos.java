    private void tabelaProdutosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelaProdutosMouseClicked
        //Pega os dados
        String codigo = tabelaProdutos.getValueAt(tabelaProdutos.getSelectedRow(), 0).toString();
        
        //busca o produto pelo códido vindo da tabela
        Produto produto = ProdutoDAO.getInstance().getById(Long.valueOf(codigo));
        
        if(produto != null ){
            setModoEdicao(true);
            jTabbedPane1.setSelectedIndex(0);
            preencherCampos(produto);
        }
        else{
            JOptionPane.showMessageDialog(null, "Funcionário não encontrado na base de dados.");
        }
        
        
    }//GEN-LAST:event_tabelaProdutosMouseClicked

    private void jTabbedPane1StateChanged(javax.swing.event.ChangeEvent evt) {//GEN-FIRST:event_jTabbedPane1StateChanged

       if(jTabbedPane1.getSelectedIndex() == 0 && modoEdicao){
           btnEditar.setVisible(true);
           btnExcluir.setVisible(true);
           btnSalvar.setVisible(true);
       }
       else{
           btnEditar.setVisible(false);
           btnExcluir.setVisible(false);
           btnSalvar.setVisible(true);
       }
        
    }//GEN-LAST:event_jTabbedPane1StateChanged

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
       String codigo = tabelaProdutos.getValueAt(tabelaProdutos.getSelectedRow(), 0).toString();
        
        //busca o funcionário pelo códido vindo da tabela
//        Funcionarios funcionario = FuncionarioDAO.getInstance().getById(Long.valueOf(codigo));
        
        ProdutoDAO.getInstance().removeById(Long.valueOf(codigo));
        
        new Utilitarios().LimpaTela(painel_dados);
        
        setModoEdicao(false);
        
        jTabbedPane1.setSelectedIndex(1);
        
    }//GEN-LAST:event_btnExcluirActionPerformed

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        Produto produto = new Produto();
       if (!StringUtils.isBlank(campoCodigo.getText())){
           produto.setId(Long.parseLong(campoCodigo.getText()));
       }
       
       produto.setId(campoCodigo != null ? Long.parseLong(campoCodigo.getText()) : null);
       produto.setDescricao(campoDescricao.getText());
       
       LocalDateTime dataCadastro = LocalDateTime.now();
       produto.setDataCadastro(dataCadastro);
        
       ProdutoDAO.getInstance().merge(produto);

       new Utilitarios().LimpaTela(painel_dados);

       setModoEdicao(false);
        
        jTabbedPane1.setSelectedIndex(1);
    }//GEN-LAST:event_btnEditarActionPerformed

