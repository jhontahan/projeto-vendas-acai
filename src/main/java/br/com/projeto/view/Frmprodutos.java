    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
       Produto produto = new Produto();
       
       Produto p = ProdutoDAO.getInstance().findByDescricao(campoDescricao.getText());
       
       if(p != null){
           JOptionPane.showMessageDialog(null, "Descrição cadastrada na base de dados!");
           return;
       }
       
       LocalDateTime dataCadastro = LocalDateTime.now();
       produto.setDataCadastro(dataCadastro);
       
       produto.setDescricao(campoDescricao.getText());
       
        
       ProdutoDAO.getInstance().persist(produto);
       
       setModoEdicao(false);
       
       new Utilitarios().LimpaTela(painel_dados);
       
    }//GEN-LAST:event_btnSalvarActionPerformed

